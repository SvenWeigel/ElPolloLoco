class Keyboard {
  /**
   * Indicates whether left movement is currently active.
   * @type {boolean}
   */
  LEFT = false;
  /**
   * Indicates whether right movement is currently active.
   * @type {boolean}
   */
  RIGHT = false;
  /**
   * Indicates whether up movement is currently active.
   * @type {boolean}
   */
  UP = false;
  /**
   * Indicates whether down movement is currently active.
   * @type {boolean}
   */
  DOWN = false;
  /**
   * Indicates whether jump (space) is currently active.
   * @type {boolean}
   */
  SPACE = false;
  /**
   * Indicates whether throw action is currently active.
   * @type {boolean}
   */
  D = false;
}
